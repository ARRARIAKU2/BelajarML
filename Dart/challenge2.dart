void main() {
  // menghitung keliling persegi
  int sisi = 10;
  int luas = 4 * sisi;
  print(luas);

  // menghitung luas lingkaran
  int jari = 10;
  double phi = 3.14;
  double luasLingkaran = phi * jari * jari;
  print(luasLingkaran);

  // mengitung volume balok
  int panjang = 10;
  int lebar = 5;
  int tinggi = 2;
  int volume = panjang * lebar * tinggi;
  print(volume);

} 